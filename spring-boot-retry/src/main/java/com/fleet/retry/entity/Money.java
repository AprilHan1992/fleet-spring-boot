package com.fleet.retry.entity;

/**
 * @author April Han
 */
public class Money {

    private String name;

    private Integer money;

    public Money(String name, Integer money) {
        this.name = name;
        this.money = money;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getMoney() {
        return money;
    }

    public void setMoney(Integer money) {
        this.money = money;
    }
}
